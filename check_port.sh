#!/bin/bash
#============================================================
# 脚本名称: check_port.sh
# 功能描述: 检查指定端口是否在监听，支持自定义端口列表
# 用法: ./check_port.sh
# 作者: ljt
# 创建日期: 2026-06-22
#============================================================

# ---------- 1. 配置区 ----------
# 可以自定义要检查的端口（空格分隔）
PORTS="80 443 22"

# ---------- 2. 初始化计数器 ----------
LISTENING=0
NOT_LISTENING=0

# ---------- 3. 输出报告头 ----------
echo "============================================="
echo "  📊 端口监听状态巡检报告"
echo "  主机: $(hostname)"
echo "  时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "  检查端口: $PORTS"
echo "============================================="

# ---------- 4. 循环检查每个端口 ----------
for PORT in $PORTS; do
    # 用 ss 检查端口是否在监听，静默输出
    ss -tunl | grep -q ":$PORT"
    
    # 判断 grep 的退出码
    if [ $? -eq 0 ]; then
        # 找到了 → 监听中
        echo -e "\033[32m✅ 端口 $PORT 正在监听\033[0m"
        LISTENING=$((LISTENING + 1))
    else
        # 没找到 → 未监听
        echo -e "\033[31m❌ 端口 $PORT 未监听\033[0m"
        NOT_LISTENING=$((NOT_LISTENING + 1))
    fi
done

# ---------- 5. 统计结果 ----------
echo "============================================="
echo "  监听端口数: $LISTENING"
echo "  未监听端口数: $NOT_LISTENING"
if [ $NOT_LISTENING -gt 0 ]; then
    echo -e "  ⚠️  状态: \033[31m存在未监听的端口\033[0m"
    exit 1
else
    echo -e "  ✅ 状态: \033[32m所有端口均在监听\033[0m"
    exit 0
fi
echo "============================================="
